    public void preencherTabela(){
        ClienteDAO clienteDAO = new ClienteDAO();
        List<Cliente> listaClientes = clienteDAO.printCliente();
        
        DefaultTableModel tabelaModel = (DefaultTableModel) jT_tabelaCliente.getModel();
        tabelaModel.setRowCount(0);
        
        for(Cliente cliente : listaClientes){
            tabelaModel.addRow(new Object[]{cliente.getId_cliente(), cliente.getNome(), cliente.getCpf(), cliente.getTelefone(), cliente.getEmail()});
        }
    }
    
