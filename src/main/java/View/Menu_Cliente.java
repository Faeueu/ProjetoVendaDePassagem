    private void jB_salvarActionPerformed(java.awt.event.ActionEvent evt) {
        Cliente cliente = new Cliente();
        cliente.setNome(jT_nome.getText());
        cliente.setCpf(jF_cpf.getText());
        cliente.setEmail(jT_email.getText());
        cliente.setTelefone(jF_telefone.getText());
        
        ClienteDAO clienteDAO = new ClienteDAO();
        clienteDAO.addCliente(cliente);
        preencherTabela();
        jTabbedPane1.setSelectedIndex(1);
        
    }

    private void jB_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_editarActionPerformed
        int opc = jT_tabelaCliente.getSelectedRow();
        if(opc >= 0){
            jT_id.setText(jT_tabelaCliente.getValueAt(opc, 0).toString());
            jT_nome.setText(jT_tabelaCliente.getValueAt(opc, 1).toString());
            jF_cpf.setText(jT_tabelaCliente.getValueAt(opc, 2).toString());
            jF_telefone.setText(jT_tabelaCliente.getValueAt(opc, 3).toString());
            jT_email.setText(jT_tabelaCliente.getValueAt(opc, 4).toString());
            
            jTabbedPane1.setSelectedIndex(0);
            jB_atualizar.setVisible(true);
            jB_salvar.setVisible(false);
        }else{
            JOptionPane.showMessageDialog(null, "Selecione um dado da tabela!");
        }
    }//GEN-LAST:event_jB_editarActionPerformed

