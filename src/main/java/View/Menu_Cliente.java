    private void jB_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_excluirActionPerformed
        int opc = jT_tabelaCliente.getSelectedRow();
        if(opc >= 0){
            Cliente cliente = new Cliente();
            cliente.setId_cliente(Integer.parseInt(jT_tabelaCliente.getValueAt(opc, 0).toString()));
            cliente.setNome(jT_tabelaCliente.getValueAt(opc, 1).toString());
            cliente.setCpf(jT_tabelaCliente.getValueAt(opc, 2).toString());
            cliente.setTelefone(jT_tabelaCliente.getValueAt(opc, 3).toString());
            cliente.setEmail(jT_tabelaCliente.getValueAt(opc, 4).toString());
            
            ClienteDAO clienteDAO = new ClienteDAO();
            clienteDAO.deleteCliente(cliente);
            preencherTabela();
        }else{
            JOptionPane.showMessageDialog(null, "Selecione um dado da tabela!");
        }
    }//GEN-LAST:event_jB_excluirActionPerformed

